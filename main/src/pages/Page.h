class Engine;
class FillSensor;
class OTAManager;

class Page
{
public:
    Page(const String &name);
    ~Page() = default;
    String name() const;
    virtual void render(Adafruit_SSD1306 &display, Engine *engine, FillSensor *FillSensor, OTAManager *otaManager) = 0;
    virtual void handleInput(DisplayController *controller, Engine *engine, DisplayController::ButtonInput input);
    virtual void reset() {};
    virtual bool autoExit() const { return true; };

protected:
    void drawTitle(Adafruit_GFX &display, const String &text, const unsigned char* iconData);
    void drawMenu(Adafruit_GFX & display, int cycle, int selection, const String &line1Text, int line1Size, const String &line2Text = String(), int line2Size = 1);

    void drawCenteredText(Adafruit_GFX & display, int cycle, const String &line1Text, int line1Size, const String &line2Text = String(), int line2Size = 1);
    void drawScrollingLabel(Adafruit_GFX &display, int cycle, const String &text, bool selected, int yOffset);
    void drawBarControl(Adafruit_GFX &display, float value, float minValue, float maxValue, int yOffset);
    
    void exit(DisplayController * controller);
    bool needsTimedRefresh() const;

private:
    String m_name;
    mutable uint16_t m_lastRefresh = 0;
};
